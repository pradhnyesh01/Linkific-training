marks = int(input("Enter your grade: "))

if (marks >= 90):
    print("Your grade is S")
elif (marks >= 80):
    print("Your grade is A")
elif (marks >= 70):
    print("Your grade is B")
elif (marks >= 60):
    print("Your grade is C")
elif (marks >= 50):
    print("Your grade is D")
else:
    print("Better luck next time!")